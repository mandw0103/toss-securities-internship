function generateMetricsByPeriod(stockId: string): Record<Period, PeriodMetrics> {
  const rand = mulberry32(hashStringToSeed(stockId));
  const result = {} as Record<Period, PeriodMetrics>;
  for (const period of PERIODS) {
    const changeRate = (rand() - 0.5) * 30;
    const tossTradingValue = Math.floor(rand() * 5_000_000_000_000) + 10_000_000_000;
    const tossTradingVolume = Math.floor(rand() * 5_000_000) + 1_000;
    const totalMultiplier = 5 + rand() * 10;
    result[period] = {
      changeRate: Math.round(changeRate * 100) / 100,
      tossTradingValue,
      tossTradingVolume,
      totalTradingValue: Math.floor(tossTradingValue * totalMultiplier),
      totalTradingVolume: Math.floor(tossTradingVolume * totalMultiplier),
    };
  }
  return result;
}

export const STOCKS: Stock[] = STOCK_METAS.map(
  ([stockId, name, market, currentPrice, tossSharePercent, aiSummary]) => ({
    stockId,
    name,
    market,
    logoSeed: name.slice(0, 1),
    currentPrice,
    tossSharePercent,
    aiSummary,
    metricsByPeriod: generateMetricsByPeriod(stockId),
  }),
);

export function fetchStocks(): Promise<Stock[]> {
  return Promise.resolve(STOCKS);
}

export function searchStocks(query: string): Promise<StockSearchResult[]> {
  if (!query) return Promise.resolve([]);
  const lower = query.toLowerCase();
  const results = STOCKS
    .filter((s) => s.name.toLowerCase().includes(lower))
    .map(({ stockId, name }) => ({ stockId, name }));
  return Promise.resolve(results);
}

export function generateSparkline(stockId: string, length = 30): number[] {
  const rand = mulberry32(hashStringToSeed(`${stockId}_sparkline`));
  const points: number[] = [];
  let value = 100;
  for (let i = 0; i < length; i++) {
    value += (rand() - 0.5) * 8;
    points.push(value);
  }
  return points;
}
